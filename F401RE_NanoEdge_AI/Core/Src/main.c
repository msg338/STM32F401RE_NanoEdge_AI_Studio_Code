/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
#ifdef NEAI
uint16_t newClass = 0;
#endif
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  /* Load the NanoEdge AI library with the trained knowledge. */
#ifdef NEAI
enum neai_state error_code = neai_classification_init( knowledge );
if (error_code != NEAI_OK)
{
  /* This happens if the knowledge does not correspond to the library
   * or if the library works into a not supported board. */
}
#endif
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_Base_Start(&htim1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    get_adc_data();
	convert_adc_data_to_mA();

#ifdef NEAI
	 /* Toggle the GREEN LED in NEAI function mode */
	 HAL_GPIO_TogglePin(GPIOA, LD2_Pin);

	 /* Call NEAI classification function */
	 neai_classification(input_user_buffer, output_class_buffer, &newClass);

	 /* UART 전송을 위한 버퍼 선언 */
	 char TX_Buffer[200];
	 int StrLen;

	 /* Print classification results */
	 StrLen = sprintf(TX_Buffer, "\nNEAI Classification Results:\r\n");
	 HAL_UART_Transmit(&huart2, (uint8_t *)TX_Buffer, StrLen, 1000);

	 for (uint16_t class_cpt = 0; class_cpt < CLASS_NUMBER; class_cpt++)
	 {
	     StrLen = sprintf(TX_Buffer, "%d: %.3f ", class_cpt, output_class_buffer[class_cpt]);
	     HAL_UART_Transmit(&huart2, (uint8_t *)TX_Buffer, StrLen, 1000);

           /* 한 줄에 3개씩 출력 (줄바꿈 추가) */
	       if ((class_cpt + 1) % 3 == 0)
	       {
	       StrLen = sprintf(TX_Buffer, "\r\n");
	       HAL_UART_Transmit(&huart2, (uint8_t *)TX_Buffer, StrLen, 1000);
	       }
	 }

	 /* Print detected class */
	 StrLen = sprintf(TX_Buffer, "\nClass found: %s\r\n", id2class[newClass]);
	 HAL_UART_Transmit(&huart2, (uint8_t *)TX_Buffer, StrLen, 1000);
#endif

#ifdef LOGGER
  	    /* Toggle the RED LED in datalogging mode  */
  		HAL_GPIO_TogglePin(GPIOA, LD2_Pin);

  		/* Print the whole buffer to the serial over usb_cdc */
  		for (uint16_t i = 0; i < SAMPLES; i++)
  		{
  			printf("%.3f ", input_user_buffer[i]);
  		}
  		/* Send carriage return char */
  		printf("\n");
#endif
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

